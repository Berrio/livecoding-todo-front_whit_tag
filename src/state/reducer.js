const reducer = (state, action) => {
  switch(action.type){
    case 'get-categories':
      return state
    case 'create-category':
      return state
    case 'deleteCategory':
      return state
    case 'add-note':
      return state
    case 'delete-note':
      return state
    case 'update-note':
      return state
  }
}

export default reducer
